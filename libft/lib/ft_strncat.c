#include "../inc/libft.h"

char	*ft_strncat(char *s1, const char *s2, size_t n)
{
	int	i;
	int	k;

	i = 0;
	k = 0;
	while (s1[i] != '\0')
		i++;
	while (n--)
	{
		s1[i] = s2[k];
		i++;
		k++;
	}
	s1[i] = '\0';
	return (s1);
}
